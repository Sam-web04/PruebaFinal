    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        String residuo = String.valueOf(CajaResiduos.getSelectedItem());
        int cantEmpleados = Integer.parseInt(this.TextCantEmpleados.getText());
        Double impacto = 0.0;
        
        if(residuo == "No impacto"){
            impacto = 0.0;
        }else if(residuo == "Contaminante Sólido"){
            impacto = 0.8;
        }else if(residuo == "Contaminante Gaseoso"){
            impacto = 1.7;
        }else if(residuo == "Contaminante Líquido"){
            impacto = 0.3;
        }
        
        impacto = cantEmpleados*impacto;
        
        mt.setValueAt(TextNombre.getText(), TablaEmpresas.getSelectedRow(), 0);
        mt.setValueAt(TextCantEmpleados.getText(), TablaEmpresas.getSelectedRow(), 1);
        mt.setValueAt(TextDireccion.getText(), TablaEmpresas.getSelectedRow(), 2);
        mt.setValueAt(CajaResiduos.getSelectedItem(), TablaEmpresas.getSelectedRow(), 3);
        mt.setValueAt((impacto), TablaEmpresas.getSelectedRow(), 4);

        JOptionPane.showMessageDialog(null, "Empresa actualizada correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        
        TextNombre.setText("");
        TextCantEmpleados.setText("");
        TextDireccion.setText("");
        CajaResiduos.setSelectedItem("");
        
        

        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void menuItem_guardarInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItem_guardarInfoActionPerformed
        
       String filePath = "D:\\Salva empresas.txt";
       File file = new File(filePath);
       
       try{
           FileWriter fw = new FileWriter(file);
           BufferedWriter bw = new BufferedWriter(fw);
           
           for(int i = 0; i < TablaEmpresas.getRowCount(); i++){
               for(int j = 0; j < TablaEmpresas.getColumnCount(); j++){
                   bw.write(TablaEmpresas.getValueAt(i, j).toString()+"; ");
               }
           }
           
           bw.close();
           fw.close();
           
           JOptionPane.showMessageDialog(null, "Información guardada correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);
           
       }catch(IOException e){
        //Logger.getLogger(NewJFrame.class.getName()).log(Level.SEVERE,null);
       }
        
    }//GEN-LAST:event_menuItem_guardarInfoActionPerformed

