    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(TextNombre.getText().trim().isEmpty() || TextCantEmpleados.getText().trim().isEmpty() || TextDireccion.getText().trim().isEmpty() || CajaResiduos.getSelectedItem() == ""){
            JOptionPane.showMessageDialog(null, "Campo vacío", "Error", JOptionPane.WARNING_MESSAGE);
        }else{
            String nombre = String.valueOf(TextNombre.getText());
            int cantEmpleados = Integer.parseInt(this.TextCantEmpleados.getText());
            String direccion = String.valueOf(TextDireccion.getText());
            String residuo = String.valueOf(CajaResiduos.getSelectedItem());
            Double impacto = 0.0;
        
            if(residuo == "No impacto"){
                impacto = 0.0;
            }else if(residuo == "Contaminante Sólido"){
                impacto = 0.8;
            }else if(residuo == "Contaminante Gaseoso"){
                impacto = 1.7;
            }else if(residuo == "Contaminante Líquido"){
                impacto = 0.3;
            }
        
            impacto = cantEmpleados*impacto;
        
            mt.addRow(new Object[]{nombre,cantEmpleados,direccion,residuo,impacto});
            JOptionPane.showMessageDialog(null, "Empresa añadida correctamente", "Aviso", JOptionPane.INFORMATION_MESSAGE);

            TextNombre.setText("");
            TextCantEmpleados.setText("");
            TextDireccion.setText("");
            CajaResiduos.setSelectedItem("");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

